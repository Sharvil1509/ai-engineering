import numpy as np

numbers = np.array([1, 2, 3, 4, 5])

print(numbers)

print("\nMultiply by 2")
print(numbers * 2)

print("\nAdd 10")
print(numbers + 10)

print("\nSquare")
print(numbers ** 2)

print("\nShape:")
print(numbers.shape)

print("\nDimensions:")
print(numbers.ndim)

print("\nSize:")
print(numbers.size)

print("\nData type:")
print(numbers.dtype)